package Chapter_06;

public class ForTest {
    public static void main(String[] args){
        for(int i=0; i<10; i++){
            System.out.println(i+" :정수캠 인공지능");
        }
        String[] name={"박","찬","웅"};

        for(int i=0; i<name.length; i++)
        {
            System.out.println(name[i]);
        }
    }
}
